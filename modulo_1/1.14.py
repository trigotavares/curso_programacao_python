tem_dinheiro = True
tem_fome = False

print(tem_dinheiro and tem_fome) # "and" aplica a conjunção, a proposição só é verdadeira se ambos o forem
print(tem_dinheiro or tem_fome) # "or" aplica a disjunção, a proposição só é verdadeira se pelo menos uma for
print(not tem_fome) # "not" aplica a negação, invertendo o valor lógico da proposição